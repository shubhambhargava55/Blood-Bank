<h1 style="color:#FF9933">Why donate blood?</h1>
<p style="color:#CCCCCC">
Blood is the part of life that is given to those who need it by those who have the resource<br> to satisfy the need. The love of fellow human and a desire to share something of oneself is what singles out a blood donor from the others.<br> Emergencies occur every minute. For each patient requiring blood, it is an emergency and the patients could have set back if blood is not available.
<br>
Your blood donation may be even more special than you realize A single donation from you can help one <br> or more patients. This is possible because whole blood <br>is made up of several useful components. These components perform special functions in your body and in the body of patients who receive your blood.
<br>
Various blood components are Red Blood Cells, White Blood Cells, Platelets, Plasma and selected Plasma Proteins. Each of these components <br>can be separated from your donated volume of blood and transfused into a specific patient requiring that particular component.<br> Thus, many can benefit from one unit of blood. Every year India requires 40 million units of 250cc blood out of which only a meager 500,000 <br> of blood units are available.
</p>

